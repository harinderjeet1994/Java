public class DecisionMaking2 {

    public static void main(String[] args){
        int number = 0;
        if (number > 0){
            System.out.println("It is a positive number");
        }
        else if (number == 0){
            System.out.println("Neither positive nor negative");
        }
        else {
            System.out.println("It is a negative number");
        }
    }
}
